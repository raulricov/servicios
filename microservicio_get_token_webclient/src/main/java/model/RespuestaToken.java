package model;

public class RespuestaToken {

	private String token;
	
	public RespuestaToken() {
		super();
	}
	
	public RespuestaToken(String token) {
		super();
		this.token = token;
	}

	public String getToken() {
		return token;
	}

	public void setToken(String token) {
		this.token = token;
	}
}